'use strict';

/*
Необходимо с помощью цикла for вывести следующие 11 строк в консоль:
0 – это ноль
1 – нечетное число
2 – четное число
3 – нечетное число
…
10 – четное число
*/

/**
 * Создаёт строковый отчёт о чётности чисел в диапазоне [0, maximum + 1], не
 * относя 0 ни к чётным, ни к нечётным числам.
 * @param {number | bigint} maximum Верхняя граница диапазона.
 * @returns {string} Отчёт в формате "'число' — это чётное/нечётное число".
 */
function getEvenReport(maximum) {
	const report = ['0 — это ноль'];
	for (let i = 1; i <= maximum; i += 2) {
		report.push(`${i} — нечётное число\n${i + 1} — чётное число`);
	}
	return report.join('\n');
}

console.log(getEvenReport(10));
